package com.javarzn.donework;

public class Calculator {
    public long plus(long n1, long n2) {
        return n1 + n2;
    }
    public long minus(long n1, long n2) {
        return n1 - n2;
    }
    public long delit(long n1, long n2) {
        return n1 / n2;
    }
    public long umnozhit(long n1, long n2) {
        return n1 * n2;
    }
}
